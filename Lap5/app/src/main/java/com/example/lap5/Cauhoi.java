package com.example.lap5;

public class Cauhoi {
    public String tenCH;
    public String dapAN;

    public Cauhoi() {
    }

    public Cauhoi(String tenCH, String dapAN) {
        this.tenCH = tenCH;
        this.dapAN = dapAN;
    }

    public String getTenCH() {
        return tenCH;
    }

    public void setTenCH(String tenCH) {
        this.tenCH = tenCH;
    }

    public String getDapAN() {
        return dapAN;
    }

    public void setDapAN(String dapAN) {
        this.dapAN = dapAN;
    }
}
